package com.customerDuplication.customerDuplicationImplementation.Models;

public class CustomerSMSDocument {
	
	private String id;
	private String name;
	private String sms;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSms() {
		return sms;
	}
	public void setSms(String sms) {
		this.sms = sms;
	}
	
}
